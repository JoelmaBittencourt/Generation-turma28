package Controle;

public class Repeticao6 {

	public static void main(String[] args) {
		int contador = 20;
		while (contador < 200) {
			System.out.println(contador);
			contador += 20;
		}
		int cont = 10;
		while (cont < 100) {
			System.out.println(cont);
			cont += 10;
		}
		int x = 5;
		while (x < 50) {
			System.out.println(x);
			x += 5;

		}
		int y = 2;
		while (y < 20) {
			System.out.println(y);
			y += 2;
		}
		int a = 1;
		while (a < 10) {
			System.out.println(a);
			a++;
		}
		int b = 1;
		while (b < 5) {
			System.out.println("Bom dia");
			b++;
		}
	}

}
